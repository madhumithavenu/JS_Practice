 /* Modules in JS
 A runtime environment is a place our program is Executed.
 In-case of JS it is either Browser or Node-js environment.

 Modules
 Writing our Server Side JS code in a single JS file is possible but impratical.
 If we create modules, break the code into pieces in seperate files, & use it on the main file is a pratical & neat Solution.
 Modules are reusable pieces of code in a file that can be exported and then imported for use in another file.
 A modular program is one whose components can be seperated, used individually, and recombined to create a complex system.*/
 